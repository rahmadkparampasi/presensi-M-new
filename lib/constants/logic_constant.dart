String monthConver(int bln) {
  late String blnT;
  if (bln == 1) {
    blnT = "JANUARI";
  } else if (bln == 2) {
    blnT = "FEBRUARI";
  } else if (bln == 3) {
    blnT = "MARET";
  } else if (bln == 4) {
    blnT = "APRIL";
  } else if (bln == 5) {
    blnT = "MEI";
  } else if (bln == 6) {
    blnT = "JUNI";
  } else if (bln == 7) {
    blnT = "JULI";
  } else if (bln == 8) {
    blnT = "AGUSTUS";
  } else if (bln == 9) {
    blnT = "SEPTEMBER";
  } else if (bln == 10) {
    blnT = "OKTOBER";
  } else if (bln == 11) {
    blnT = "NOVEMBER";
  } else if (bln == 12) {
    blnT = "DESEMBER";
  }
  return blnT;
}
